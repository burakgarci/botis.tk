<h1>Komutlar</h1>
<pre>
Lütfen dikkate alın.

<strong>ID:</strong> <em>(İng. identification)</em> Nimbuzz kullanıcı adı. Ayrıca nick ya da nik diye de bilinir.

Büyük harflerle yazılan kısımlar değişkendir. Kendiniz ilgili numarayı, kelimeyi ya da cümleyi onun yerine yazacaksınız. Örneğin aşağıda <code>pr ID</code> yazılmışsa ve işlem yapmak istediğiniz ID <em>portakal</em> ise siz <code>pr portakal</code> yazmalısınız.
</pre>
<ul class="cmd-list">
	<li>
		<span>Yardım</span>
		<p>Bu yardım konularını listelemek için <code>yardım</code> ya da <code>komutlar</code> yazın</p>
	</li>
	<li>
		<span>İsim kaydetme</span>
		<p>Kendi isminizi kaydetmek için <code>adım ADINIZ</code> ya da <code>ismim ADINIZ</code> komutlarını kullanabilirsiniz. Sadece tek bir bir kelime kaydedilebilir. İsim en fazla 15 karakter olabilir ve Türk alfabesi dışında bir karakter barındıramaz. İsmin ilk harfi büyük diğerleri küçük olarak kaydedilir.</p>
	</li>
	<li>
		<span>Karşılama mesajı (Hoş geldin mesajı)</span>
		<p>Karşılama mesajı ya da hoş geldin mesajı, siz herhangi bir odaya girdiğinizde Botişko'nun göndereceği mesajdır. Karşılama mesajınızı ayarlamak ya da değiştirmek için <code>hgm MESAJINIZ</code> komutunu, mesajı kapatmak için <code>-hgm</code> komutunu, kayıtlı bir mesaj varsa ve mesaj kapalıysa açmak için <code>+hgm</code> komutunu kullanın.</p>
	</li>
	<li>
		<span>Avatar görüntüleme</span>
		<p>Bir kullanıcının tam boy avatarını görüntülemek için <code>av ID</code> komutunu kullanabilirsiniz.</p>
		<p>iOS ve Andorid gibi işletim sistemi cihazlarda odalardaki kişilerin avatarları büyük boyutta görüntülenebiliyor olsa da bazı cihazlarda avatarları tam boyutta görüntüleme imkanı yok.</p>
	</li>
	<li>
		<span>Durum raporu</span>
		<p>Botun kaç odada olduğunu, listesinde kça kişi çevrimiçi olduğu gibi mevcut durumu ile ilgili bazı bilgilere <code>dp</code> yazarak ulaşabilirsiniz.</p>
	</li>
	<li>
		<span>Ek attırma ve sildirme</span>
		<p>Botişko'nun sizi eklemesini istiyorsanız <code>ekle</code> yazmanız yeterli. Sizi silmesini istiyorsanız <code>sil</code> yazabilirsiniz.</p>
	</li>
	<li>
		<span>Botişko'yu odaya davet etme</span>
		<p>Botişko'nun odanıza girmesini istiyorsanız <code>gel ODA</code> ya da <code>gir ODA</code> yazabilir veya eğer Botişko kişi listenizdeyse odadan direkt kişi daveti yapabilirsiniz. Eğer Botişko odaya girmişse herhangi bir almazsınz. Odaya üye değil ise kod (<a href="https://tr.wikipedia.org/wiki/CAPTCHA" target="_blank">CAPTCHA</a>) girmeniz gerekir. CAPTCHA girmek çok can sıkıcıdır o yüzden Botişko'yu odaya üye yaptıktan sonra davet etmenız mantıklı olacaktır. CAPTCHA dışında bir sıkıntı sorun oluşursa Botişko  size bildirecektir.</p>
	</li>
	<li>
		<span>Odaya kişi davet etme</span>
		<p>Bulunduğunuz oda bir kişiyi davet etmek için <code>dt ID</code> komutunu kullanabilirsiniz. Eğer kişi Botişko'nun listesindeyse ya da Botişko'nun bulunduğu bir odadaysa davetiniz kişiye ulaştırılacaktır.</p>
	</li>
	<li>
		<span>Hava durumu</span>
		<p>Bir il, ilçe için hava durumu dorgulamak için <code>hd il</code> yazın. Dünyanın herhangi bir yerinin hava durumunu sorgulayabilirsiniz. Örneğin <code>hd Kudüs</code> gibi.</p>
	</li>
	<li>
		<span>İLeti görüntüleme</span>
		<p><code>il</code> yazdıktan sonra iletisini görüntülemek istediğiniz kişinin Nimbuzz IDsini yazın. Örneğin <code>il botisko</code> gibi.</p>
	</li>
	<li>
		<span>Kayıt tarihi görüntüleme</span>
		<p>Bir kullanıcının Nimbuzz'a ne zaman kayıt olduğunu öğrenmek için <code>ka ID</code> yazabilirsiniz. Kendi kayıt tarihinizi görüntülemek için sadece <code>ka</code> yazmanız yeterli.</p>
	</li>
	<li>
		<span>Profil görüntüleme</span>
		<p>Profil bilgilerini görüntülemek istediğiniz kişinin IDsini <code>pr</code> yazdıktan sonra yazın. <code>pr botisko</code> gibi.</p>
	</li>
	<li>
		<span>Kayıtlı isminizi öğrenme</span>
		<p>Botişko'da isminiz kayıtlı ise <code>kimim</code> ya da <code>ben kimim</code> yazarak öğrenebilirisiniz.</p>
	</li>
	<li>
		<span>Botişko'nun moderatörlerini listeleme</span>
		<p><code>modli</code></p>
	</li>
	<li>
		<span>Botişko'nun yöneticilerini listeleme</span>
		<p><code>yonli</code></p>
	</li>
	<li>
		<span>Saat</span>
		<p>O anki Türkiye saatini öğrenmek için <code>saat</code> yazın.</p>
	</li>
	<li>
		<span>Tarih</span>
		<p>Tarihi <em style="color: orange">Bugün 4 Temmuz 2015, Cumartesi ve saat 19:52:14</em> şeklinde söyletmek için <code>tarih</code> yazın.</p>
	</li>
	<li>
		<span>İftara kalan süre</span>
		<p>Sadece iller için geçerlidir. Sonuç alamıyorsanız lütfen il ismini büyük harflerle ya da Türkçe karakter kullanamdan yazın. Komut: <code>iftar İL</code></p>
	</li>
	<li>
		<span>İmsağa kalan süre</span>
		<p>Sadece iller için geçerlidir. Sonuç alamıyorsanız lütfen il ismini büyük harflerle ya da Türkçe karakter kullanamdan yazın. Komut: <code>imsak İL</code></p>
	</li>
	<li>
		<span>Konuşturma</span>
		<p>Botişko'ya bir şeyler söyletmek için <code>de BİR ŞEYLER</code> yazabilirsiniz. Bu komut odalarda kötü amaçlı kullanılabileceğinden dolayı odalar için devre dışı bırakılmıştır.</p>
	</li>
	<li>
		<span>İsimden kişi sorgulama</span>
		<p>Bir kişinin ismini sorarar IDsini öğrenmek için <code>İSİM kim</code> yazabilirsiniz. Odada sorgulamak için mesajı başına ya da sonuna <em>bot</em> eklemelisiniz.</p>
	</li>
	<li>
		<span>Kişi durumunu öğrenme ve odalarda kişi arama</span>
		<p>İsim veya ID ile yapacağınız sorgulamada, o isimdeki kişilerin veya IDsi girilen kullanıcının, eğer kişi Botişko'nun listesindeyse durumu ile beraber bulunduğu odaları, eğer Botişko'nun listesinde değilse bulunduğu odaları söyler.</p>
		<pre>(18:05:37) siz: burak nerde bot <br>(18:05:37) botisko: yorgun: meşgul (Linux) türkiye-botişko-test</pre>
	</li>
	<li>
		<span>Odalarda kullanıcı arama</span>
		<p>IDsini gireceğiniz kullanıcının hangi odalarda olduğunu söyler. İsim ile arama yapılamaz. Botişko'nun bulunduğu odalarda IDsi girilen kullanıcıyı arar. Komut: <code>ara ID</code></p>
	</li>
	<li>
		<span>Oyun Komutları</span>
		<p>Botişko'da şu an için sadece bir tek oyun vardır. Karıştırılan kelimeyi bulma...</p>
		<ul>
			<li>
				<span>Oyun başlatma</span>
				<p>Bir odada oyun başlatma için şu komutlardan birini kullanabilirsiniz: <code>kelime oyunu</code>, <code>oyun oyna</code>, <code>kelime bulmaca</code>.</p>
				<p>Oyun sadece oynanabilir. Oyun sadece oda ya da Botişko'nun moderatörleri tarafından başlatılabilir.</p>
			</li>
			<li>
				<span>Kelimeyi karıştırma</span>
				<p>Mevcut kelimeyi karıştırmak için <code>karıştır</code> yazmalısınız. Odada oynanan bir oyun yoksa herhangi bir cevap almazsınız.</p>
			</li>
			<li>
				<span>Kelimeyi değiştirme</span>
				<p><code>cevap ne</code> ya da <code>yeni kelime</code> yazarak mevcut kelimeyi değiştirebilirsiniz. Kelimeyi değiştirdiğinizde puan kesilir.</p>
			</li>
			<li>
				<span>Skor</span>
				<p>Oynanıyor olan ya da en son oynanmış oyunun puanlamasını görüntülemek için <code>skor</code> yazabilirsiniz.</p>
			</li>
			<li>
				<span>Oyunu bitirme</span>
				<p>Oyunu bitirmek için <code>oyunu bitir</code> yazmalısınız. Oyunu sadece oyun yöneticisi ya da Botişko'nun yöneticileri bitirebilir. Oyunu yöneticisi oyunu başlatan kişidir ve mevcut oyun yöneticisi odadan çıkarsa oyunu oynayanlar öncelikli olmak üzere odadaki kullanıcılar arasından başka bir yönetici seçilir.</p>
			</li>
		</ul>
	</li>	
